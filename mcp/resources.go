// serviceNames returns the sorted list of service keys from the registry.
func serviceNames() []string {
	names := make([]string, 0, len(serviceRegistry))
	for k := range serviceRegistry {
		names = append(names, k)
	}
	sort.Strings(names)
	return names
}

func (s *Server) registerResources() {
	// Static resource: list all service names.
	s.mcp.AddResource(&gomcp.Resource{
		URI:         "jni://services",
		Name:        "Android Service List",
		Description: "Lists all available Android service names exposed via gRPC.",
		MIMEType:    "application/json",
	}, s.handleListServices)

	// Resource template: methods for a specific service.
	s.mcp.AddResourceTemplate(&gomcp.ResourceTemplate{
		URITemplate: "jni://services/{service}",
		Name:        "Service Methods",
		Description: "Lists RPC methods available on a specific Android service.",
		MIMEType:    "application/json",
	}, s.handleServiceMethods)
}

func (s *Server) handleListServices(_ context.Context, req *gomcp.ReadResourceRequest) (*gomcp.ReadResourceResult, error) {
	data, err := json.Marshal(serviceNames())
	if err != nil {
		return nil, fmt.Errorf("marshal service list: %w", err)
	}
	return &gomcp.ReadResourceResult{
		Contents: []*gomcp.ResourceContents{{
			URI:      req.Params.URI,
			MIMEType: "application/json",
			Text:     string(data),
		}},
	}, nil
}

func (s *Server) handleServiceMethods(_ context.Context, req *gomcp.ReadResourceRequest) (*gomcp.ReadResourceResult, error) {
	// Extract service key from the URI "jni://services/<key>".
	const prefix = "jni://services/"
	if !strings.HasPrefix(req.Params.URI, prefix) {
		return nil, fmt.Errorf("unexpected URI format: %s", req.Params.URI)
	}
	key := strings.TrimPrefix(req.Params.URI, prefix)

	methods, ok := serviceRegistry[key]
	if !ok {
		// Try partial match: the caller may pass just the package name.
		var matched []string
		for k := range serviceRegistry {
			if strings.HasPrefix(k, key+"/") || k == key {
				matched = append(matched, k)
			}
		}
		if len(matched) == 0 {
			return nil, fmt.Errorf("unknown service: %s", key)
		}
		sort.Strings(matched)
		result := make(map[string][]string, len(matched))
		for _, m := range matched {
			result[m] = serviceRegistry[m]
		}
		data, err := json.Marshal(result)
		if err != nil {
			return nil, fmt.Errorf("marshal methods: %w", err)
		}
		return &gomcp.ReadResourceResult{
			Contents: []*gomcp.ResourceContents{{
				URI:      req.Params.URI,
				MIMEType: "application/json",
				Text:     string(data),
			}},
		}, nil
	}

	data, err := json.Marshal(methods)
	if err != nil {
		return nil, fmt.Errorf("marshal methods: %w", err)
	}
	return &gomcp.ReadResourceResult{
		Contents: []*gomcp.ResourceContents{{
			URI:      req.Params.URI,
			MIMEType: "application/json",
			Text:     string(data),
		}},
	}, nil
}

