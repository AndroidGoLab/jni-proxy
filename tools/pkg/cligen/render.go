// renderPackage renders a CLIPackage to a Go source file.
func renderPackage(pkg *CLIPackage, outputPath string) error {
	// Determine if any command uses streaming (to include "io" import).
	hasStreaming := false
	for _, svc := range pkg.Services {
		for _, cmd := range svc.Commands {
			if cmd.ServerStreaming {
				hasStreaming = true
			}
		}
	}

	data := struct {
		*CLIPackage
		HasStreaming bool
	}{
		CLIPackage:   pkg,
		HasStreaming: hasStreaming,
	}

	var buf bytes.Buffer
	if err := packageTemplate.Execute(&buf, data); err != nil {
		return fmt.Errorf("execute template: %w", err)
	}

	// Run gofmt on the generated code; fall back to unformatted if it fails.
	out := buf.Bytes()
	formatted, err := format.Source(out)
	if err != nil {
		formatted = out
	}

	if err := os.WriteFile(outputPath, formatted, 0o644); err != nil {
		return fmt.Errorf("write %s: %w", outputPath, err)
	}
	return nil
}
