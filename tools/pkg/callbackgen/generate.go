package callbackgen

import (
	"bytes"
	"fmt"
	"os"
	"path/filepath"
	"strings"
	"text/template"
)

const javaPackage = "center.dx.jni.generated"

// templateData holds the context passed to the Java adapter template.
type templateData struct {
	Package string
	Entry   CallbackEntry
}

// Generate reads the spec and writes one Java adapter file per callback entry.
func Generate(specPath, outputDir string) error {
	spec, err := LoadSpec(specPath)
	if err != nil {
		return fmt.Errorf("load spec: %w", err)
	}

	if err := os.MkdirAll(outputDir, 0o755); err != nil {
		return fmt.Errorf("mkdir %s: %w", outputDir, err)
	}

	for _, entry := range spec.Callbacks {
		if err := generateAdapter(entry, outputDir); err != nil {
			return fmt.Errorf("generate %s: %w", entry.Adapter, err)
		}
	}

	return nil
}

func generateAdapter(entry CallbackEntry, outputDir string) error {
	data := templateData{
		Package: javaPackage,
		Entry:   entry,
	}

	tmpl := adapterTemplate()

	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, data); err != nil {
		return fmt.Errorf("execute template: %w", err)
	}

	outputPath := filepath.Join(outputDir, entry.Adapter+".java")
	if err := os.WriteFile(outputPath, buf.Bytes(), 0o644); err != nil {
		return fmt.Errorf("write %s: %w", outputPath, err)
	}

	return nil
}

// adapterTemplate builds the template with all functions registered.
func adapterTemplate() *template.Template {
	funcMap := template.FuncMap{
		"boxExpr":     BoxExpression,
		"isPrimitive": IsPrimitive,
		"inheritance": inheritanceKeyword,
		"argsList":    renderArgsList,
		"paramDecl":   renderParamDecl,
	}

	return template.Must(template.New("adapter").Funcs(funcMap).Parse(adapterTemplateText))
}

const adapterTemplateText = `package {{ .Package }};

import center.dx.jni.internal.GoAbstractDispatch;

/**
 * Generated by callbackgen. DO NOT EDIT.
 * Adapter for {{ .Entry.Class }}
 */
public class {{ .Entry.Adapter }}
        {{ inheritance .Entry }} {{ .Entry.Class }} {

    private final long handlerID;

    public {{ .Entry.Adapter }}(long handlerID) {
        this.handlerID = handlerID;
    }
{{ range .Entry.Methods }}
    @Override
    public void {{ .Name }}({{ paramDecl .Params }}) {
        GoAbstractDispatch.invoke(handlerID, "{{ .Name }}", new Object[]{{ "{" }}{{ argsList .Params }}{{ "}" }});
    }
{{ end }}}
`

// inheritanceKeyword returns "implements" for interfaces, "extends" for classes.
func inheritanceKeyword(entry CallbackEntry) string {
	if entry.Interface {
		return "implements"
	}
	return "extends"
}

// renderParamDecl renders the Java method parameter declaration list.
// e.g. "android.hardware.camera2.CameraDevice camera, int error"
func renderParamDecl(params []ParamEntry) string {
	parts := make([]string, len(params))
	for i, p := range params {
		parts[i] = p.Type + " " + p.Name
	}
	return strings.Join(parts, ", ")
}

// renderArgsList renders the boxed arguments for the Object[] array.
// e.g. "camera, Integer.valueOf(error)"
func renderArgsList(params []ParamEntry) string {
	parts := make([]string, len(params))
	for i, p := range params {
		parts[i] = BoxExpression(p.Type, p.Name)
	}
	return strings.Join(parts, ", ")
}
