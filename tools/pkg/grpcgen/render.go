// serverImportEntry is used by the server template to render unique imports.
type serverImportEntry struct {
	Alias string
	Path  string
}

// serverUniqueImports collects unique (alias, path) pairs from services.
func serverUniqueImports(services []ServerService) []serverImportEntry {
	seen := make(map[string]bool)
	var result []serverImportEntry
	for _, svc := range services {
		if seen[svc.GoImport] {
			continue
		}
		seen[svc.GoImport] = true
		result = append(result, serverImportEntry{
			Alias: svc.ImportAlias,
			Path:  svc.GoImport,
		})
	}
	return result
}

// lower returns the first character lowered.
func lower(s string) string {
	if s == "" {
		return ""
	}
	first := s[0]
	if first >= 'A' && first <= 'Z' {
		return string(first-'A'+'a') + s[1:]
	}
	return s
}

// renderServer renders a ServerData to Go source code written to outputPath.
func renderServer(data *ServerData, outputPath string) error {
	src, err := renderServerToString(data)
	if err != nil {
		return err
	}
	return writeFormattedGo(outputPath, []byte(src))
}

// renderServerToString renders a ServerData to Go source code as a string.
func renderServerToString(data *ServerData) (string, error) {
	var buf bytes.Buffer
	if err := serverTemplate.Execute(&buf, data); err != nil {
		return "", fmt.Errorf("execute template: %w", err)
	}
	return buf.String(), nil
}

