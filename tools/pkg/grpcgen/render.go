// renderClient renders a ClientData to Go source code written to outputPath.
func renderClient(data *ClientData, outputPath string) error {
	src, err := renderClientToString(data)
	if err != nil {
		return err
	}
	return writeFormattedGo(outputPath, []byte(src))
}

// renderClientToString renders a ClientData to Go source code as a string.
func renderClientToString(data *ClientData) (string, error) {
	// Build data class map for template access.
	dcMap := make(map[string][]ClientDataClassField)
	for _, dc := range data.DataClasses {
		dcMap[dc.GoType] = dc.Fields
	}

	renderData := clientRenderData{
		Package:      data.Package,
		GoModule:     data.GoModule,
		Services:     data.Services,
		DataClasses:  data.DataClasses,
		DataClassMap: dcMap,
	}

	var buf bytes.Buffer
	if err := clientTemplate.Execute(&buf, renderData); err != nil {
		return "", fmt.Errorf("execute template: %w", err)
	}
	return buf.String(), nil
}

// writeFormattedGo runs go/format on src and writes the result to outputPath.
// If formatting fails (e.g. the generated code has a syntax error), the
// unformatted source is written as a fallback so the user can debug.
func writeFormattedGo(outputPath string, src []byte) error {
	formatted, err := format.Source(src)
	if err != nil {
		// Fall back to unformatted output so the user can inspect the error.
		formatted = src
	}
	if err := os.WriteFile(outputPath, formatted, 0o644); err != nil {
		return fmt.Errorf("write %s: %w", outputPath, err)
	}
	return nil
}
