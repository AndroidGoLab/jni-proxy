func TestIsServiceEligible(t *testing.T) {
	oneMethod := []javagen.MergedMethod{{GoName: "Foo"}}
	tests := []struct {
		name          string
		cls           javagen.MergedClass
		expectService bool // IsServiceEligible (proto + client)
		expectServer  bool // IsServerEligible (server impl)
	}{
		{
			name:          "system_service with methods",
			cls:           javagen.MergedClass{Obtain: "system_service", Methods: oneMethod},
			expectService: true,
			expectServer:  true,
		},
		{
			name:          "system_service without methods",
			cls:           javagen.MergedClass{Obtain: "system_service"},
			expectService: false,
			expectServer:  false,
		},
		{
			name:          "context_method with methods",
			cls:           javagen.MergedClass{Obtain: "context_method", Methods: oneMethod},
			expectService: true,
			expectServer:  false,
		},
		{
			name:          "constructor with methods",
			cls:           javagen.MergedClass{Obtain: "constructor", Methods: oneMethod},
			expectService: true,
			expectServer:  true,
		},
		{
			name:          "static with methods",
			cls:           javagen.MergedClass{Obtain: "static", Methods: oneMethod},
			expectService: true,
			expectServer:  false,
		},
		{
			name:          "no obtain with methods",
			cls:           javagen.MergedClass{Obtain: "", Methods: oneMethod},
			expectService: true,
			expectServer:  false,
		},
		{
			name:          "data_class",
			cls:           javagen.MergedClass{Kind: "data_class", Obtain: "system_service", Methods: oneMethod},
			expectService: false,
			expectServer:  false,
		},
		{
			name:          "builder",
			cls:           javagen.MergedClass{Kind: "builder", Methods: oneMethod},
			expectService: false,
			expectServer:  false,
		},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			got := protogen.IsServiceEligible(tt.cls)
			if got != tt.expectService {
				t.Errorf("IsServiceEligible() = %v, want %v", got, tt.expectService)
			}
			got = protogen.IsServerEligible(tt.cls)
			if got != tt.expectServer {
				t.Errorf("IsServerEligible() = %v, want %v", got, tt.expectServer)
			}
		})
	}
}

func TestBuildCallArgs(t *testing.T) {
	tests := []struct {
		name        string
		params      []javagen.MergedParam
		want        string
		wantHandles bool
	}{
		{
			name:   "empty",
			params: nil,
			want:   "",
		},
		{
			name: "string param",
			params: []javagen.MergedParam{
				{GoName: "provider", GoType: "string", IsString: true},
			},
			want: "req.GetProvider()",
		},
		{
			name: "bool param",
			params: []javagen.MergedParam{
				{GoName: "enabledOnly", GoType: "bool", IsBool: true},
			},
			want: "req.GetEnabledOnly()",
		},
		{
			name: "object param",
			params: []javagen.MergedParam{
				{GoName: "listener", GoType: "*jni.Object", IsObject: true},
			},
			want:        "s.Handles.Get(req.GetListener())",
			wantHandles: true,
		},
		{
			name: "mixed params",
			params: []javagen.MergedParam{
				{GoName: "provider", GoType: "string", IsString: true},
				{GoName: "minTimeMs", GoType: "int64"},
				{GoName: "listener", GoType: "*jni.Object", IsObject: true},
			},
			want:        "req.GetProvider(), req.GetMinTimeMs(), s.Handles.Get(req.GetListener())",
			wantHandles: true,
		},
		{
			name: "android.content.Context auto-substituted",
			params: []javagen.MergedParam{
				{GoName: "arg0", GoType: "*jni.Object", IsObject: true, JavaType: "android.content.Context"},
			},
			want:        "s.Ctx.Obj",
			wantHandles: false,
		},
		{
			name: "context with other params",
			params: []javagen.MergedParam{
				{GoName: "arg0", GoType: "*jni.Object", IsObject: true, JavaType: "android.content.Context"},
				{GoName: "arg1", GoType: "string", IsString: true},
			},
			want:        "s.Ctx.Obj, req.GetArg1()",
			wantHandles: false,
		},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			got, gotHandles := buildCallArgs(tt.params)
			if got != tt.want {
				t.Errorf("buildCallArgs() = %q, want %q", got, tt.want)
			}
			if gotHandles != tt.wantHandles {
				t.Errorf("buildCallArgs() needsHandles = %v, want %v", gotHandles, tt.wantHandles)
			}
		})
	}
}

