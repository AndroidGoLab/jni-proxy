// loadSpec reads a spec YAML from the embedded spec.Java filesystem.
func loadSpec(t *testing.T, name string) *javagen.Spec {
	t.Helper()
	data, err := spec.Java.ReadFile("java/" + name)
	if err != nil {
		t.Fatalf("reading embedded spec %s: %v", name, err)
	}
	s, err := javagen.ParseSpec(data)
	if err != nil {
		t.Fatalf("parsing spec %s: %v", name, err)
	}
	return s
}

// loadOverlay reads an overlay YAML from the embedded spec.Overlays filesystem.
// Returns an empty overlay if the file does not exist.
func loadOverlay(t *testing.T, name string) *javagen.Overlay {
	t.Helper()
	data, err := spec.Overlays.ReadFile("overlays/java/" + name)
	if err != nil {
		return &javagen.Overlay{}
	}
	ov, err := javagen.ParseOverlay(data)
	if err != nil {
		t.Fatalf("parsing overlay %s: %v", name, err)
	}
	return ov
}

func TestBuildServerData_Location(t *testing.T) {
	sp := loadSpec(t, "location.yaml")
	overlay := loadOverlay(t, "location.yaml")
	merged, err := javagen.Merge(sp, overlay)
	if err != nil {
		t.Fatalf("merge: %v", err)
	}

	goModule := "github.com/AndroidGoLab/jni"
	protoData := protogen.BuildProtoData(merged, goModule)
	data := buildServerData(merged, goModule, goModule, protoData, emptyGoNames())

	if data.Package != "location" {
		t.Errorf("Package = %q, want %q", data.Package, "location")
	}
	if data.GoModule != "github.com/AndroidGoLab/jni" {
		t.Errorf("GoModule = %q, want %q", data.GoModule, "github.com/AndroidGoLab/jni")
	}
	if len(data.Services) == 0 {
		t.Fatal("Services count = 0, want at least 1")
	}

	var svc *ServerService
	for i := range data.Services {
		if data.Services[i].GoType == "Manager" {
			svc = &data.Services[i]
			break
		}
	}
	if svc == nil {
		t.Fatal("Manager service not found among services")
	}
	if svc.ServiceName != "ManagerService" {
		t.Errorf("Service ServiceName = %q, want %q", svc.ServiceName, "ManagerService")
	}
	if !svc.Close {
		t.Error("Service Close should be true")
	}

	// Check that methods include GetLastKnownLocation and IsProviderEnabled.
	// Note: unexported spec methods like getProvidersRaw are skipped.
	methodNames := make(map[string]bool)
	for _, m := range svc.Methods {
		methodNames[m.GoName] = true
	}
	for _, want := range []string{"GetLastKnownLocation", "IsProviderEnabled"} {
		if !methodNames[want] {
			t.Errorf("missing method %q in service", want)
		}
	}
	// Verify unexported spec methods are excluded.
	for _, unwanted := range []string{"GetProvidersRaw"} {
		if methodNames[unwanted] {
			t.Errorf("unexported spec method %q should not be in service", unwanted)
		}
	}
}

