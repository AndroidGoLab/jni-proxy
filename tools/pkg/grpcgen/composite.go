package grpcgen

import (
	"bytes"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"sort"
	"text/template"
)

// CompositeEntry describes a generated per-package server or client
// for use in the composite registration/client file.
type CompositeEntry struct {
	Package      string // Go package name, e.g. "location"
	GoType       string // Server type name, e.g. "Manager"
	ServiceName  string // Proto service name, e.g. "ManagerService"
	NeedsHandles bool   // Whether the service needs an object handle store
}

// compositeFieldName returns a unique struct field name for a composite entry
// by combining the package name and GoType (e.g., "AlarmManager",
// "TvInputManager") to avoid collisions when different packages share
// the same GoType.
func compositeFieldName(e CompositeEntry) string {
	return exportName(e.Package) + exportName(e.GoType)
}

// GenerateCompositeServer generates grpc/server/server.go which registers all
// per-package gRPC service servers.
func GenerateCompositeServer(entries []CompositeEntry, outputDir, goModule, jniModule string) error {
	if len(entries) == 0 {
		return nil
	}

	sort.Slice(entries, func(i, j int) bool {
		return entries[i].Package < entries[j].Package
	})

	needsHandles := false
	for _, e := range entries {
		if e.NeedsHandles {
			needsHandles = true
			break
		}
	}

	data := compositeServerData{
		GoModule:     goModule,
		JniModule:    jniModule,
		Entries:      entries,
		NeedsHandles: needsHandles,
	}

	var buf bytes.Buffer
	if err := compositeServerTemplate.Execute(&buf, data); err != nil {
		return fmt.Errorf("execute composite server template: %w", err)
	}

	serverDir := filepath.Join(outputDir, "grpc", "server")
	if err := os.MkdirAll(serverDir, 0o755); err != nil {
		return fmt.Errorf("mkdir grpc/server: %w", err)
	}
	outPath := filepath.Join(serverDir, "server.go")

	out := buf.Bytes()
	formatted, fmtErr := format.Source(out)
	if fmtErr != nil {
		formatted = out
	}
	if err := os.WriteFile(outPath, formatted, 0o644); err != nil {
		return fmt.Errorf("write %s: %w", outPath, err)
	}
	return nil
}

// GenerateCompositeClient generates grpc/client/client.go which provides
// a unified Client with all per-package clients.
func GenerateCompositeClient(entries []CompositeEntry, outputDir, goModule string) error {
	if len(entries) == 0 {
		return nil
	}

	sort.Slice(entries, func(i, j int) bool {
		return entries[i].Package < entries[j].Package
	})

	data := compositeClientData{
		GoModule: goModule,
		Entries:  entries,
	}

	var buf bytes.Buffer
	if err := compositeClientTemplate.Execute(&buf, data); err != nil {
		return fmt.Errorf("execute composite client template: %w", err)
	}

	clientDir := filepath.Join(outputDir, "grpc", "client")
	if err := os.MkdirAll(clientDir, 0o755); err != nil {
		return fmt.Errorf("mkdir grpc/client: %w", err)
	}
	outPath := filepath.Join(clientDir, "client.go")

	out := buf.Bytes()
	formatted, fmtErr := format.Source(out)
	if fmtErr != nil {
		formatted = out
	}
	if err := os.WriteFile(outPath, formatted, 0o644); err != nil {
		return fmt.Errorf("write %s: %w", outPath, err)
	}
	return nil
}

var compositeServerTemplate = template.Must(template.New("composite_server").Funcs(template.FuncMap{
	"export": exportName,
}).Parse(`// Code generated by grpcgen. DO NOT EDIT.

package server

import (
	"{{.JniModule}}/app"
	"{{.GoModule}}/handlestore"
	handlepb "{{.GoModule}}/proto/handlestore"
	"google.golang.org/grpc"
{{range .Entries}}
	{{.Package}}server "{{$.GoModule}}/grpc/server/{{.Package}}"
	{{.Package}}pb "{{$.GoModule}}/proto/{{.Package}}"
{{- end}}
)

// RegisterAll registers all generated gRPC service servers.
// The handles parameter provides the shared object handle store for services
// that pass JNI object references over gRPC.
func RegisterAll(s grpc.ServiceRegistrar, ctx *app.Context, handles *handlestore.HandleStore) {
	handlepb.RegisterHandleStoreServiceServer(s, &handlestore.Server{VM: ctx.VM, Handles: handles})
{{- range .Entries}}
	{{.Package}}pb.Register{{.ServiceName}}Server(s, &{{.Package}}server.{{export .GoType}}Server{Ctx: ctx{{if .NeedsHandles}}, Handles: handles{{end}}})
{{- end}}
}
`))

var compositeClientTemplate = template.Must(template.New("composite_client").Funcs(template.FuncMap{
	"export":    exportName,
	"fieldName": compositeFieldName,
}).Parse(`// Code generated by grpcgen. DO NOT EDIT.

package client

import (
	"context"

	handlepb "{{.GoModule}}/proto/handlestore"
	"google.golang.org/grpc"
{{range .Entries}}
	{{.Package}}client "{{$.GoModule}}/grpc/client/{{.Package}}"
{{- end}}
)

// Client provides access to all Android API services over gRPC.
type Client struct {
	handles handlepb.HandleStoreServiceClient
{{- range .Entries}}
	{{fieldName .}} *{{.Package}}client.{{export .GoType}}Client
{{- end}}
}

// NewClient creates a composite client from a gRPC connection.
func NewClient(cc grpc.ClientConnInterface) *Client {
	return &Client{
		handles: handlepb.NewHandleStoreServiceClient(cc),
{{- range .Entries}}
		{{fieldName .}}: {{.Package}}client.New{{export .GoType}}Client(cc),
{{- end}}
	}
}

// ReleaseHandle releases a server-side JNI object handle. Call this when
// you are done using an object handle returned by an RPC method.
func (c *Client) ReleaseHandle(ctx context.Context, handle int64) error {
	_, err := c.handles.ReleaseHandle(ctx, &handlepb.ReleaseHandleRequest{Handle: handle})
	return err
}
`))
