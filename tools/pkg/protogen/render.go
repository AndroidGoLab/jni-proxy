package protogen

import (
	"bytes"
	"fmt"
	"os"
	"text/template"
)

// protoTemplate is the Go text/template for rendering a .proto file.
const protoTemplate = `// Code generated by protogen. DO NOT EDIT.

syntax = "proto3";

package {{.ProtoPackage}};

option go_package = "{{.GoPackage}}";
{{range .Messages}}
message {{.Name}} {
{{- range .Fields}}
  {{if .Optional}}optional {{end}}{{.Type}} {{.Name}} = {{.Number}};
{{- end}}
}
{{end}}
{{- range .Services}}
service {{.Name}} {
{{- range .RPCs}}
  {{- if .Comment}}
  // {{.Comment}}
  {{- end}}
  rpc {{.Name}} ({{if .ClientStreaming}}stream {{end}}{{.InputType}}) returns ({{if .ServerStreaming}}stream {{end}}{{.OutputType}});
{{- end}}
}
{{end}}`

// renderProto renders a ProtoData to a .proto file at the given path.
func renderProto(data *ProtoData, outputPath string) error {
	tmpl, err := template.New("proto").Parse(protoTemplate)
	if err != nil {
		return fmt.Errorf("parse template: %w", err)
	}

	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, data); err != nil {
		return fmt.Errorf("execute template: %w", err)
	}

	if err := os.WriteFile(outputPath, buf.Bytes(), 0o644); err != nil {
		return fmt.Errorf("write %s: %w", outputPath, err)
	}

	return nil
}

// renderProtoToString renders a ProtoData to a string (for testing).
func renderProtoToString(data *ProtoData) (string, error) {
	tmpl, err := template.New("proto").Parse(protoTemplate)
	if err != nil {
		return "", fmt.Errorf("parse template: %w", err)
	}

	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, data); err != nil {
		return "", fmt.Errorf("execute template: %w", err)
	}

	return buf.String(), nil
}
