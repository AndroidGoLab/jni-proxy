package protogen

import (
	"os"
	"path/filepath"
	"strings"
	"testing"
)

// findJNIRepoRoot locates the jni repo root by walking up from cwd to find
// the go.mod file (jni-proxy root), then looking for the sibling jni directory
// that contains the spec/ directory.
func findJNIRepoRoot(t *testing.T) string {
	t.Helper()
	dir, err := os.Getwd()
	if err != nil {
		t.Fatalf("getting cwd: %v", err)
	}
	proxyRoot := dir
	for {
		if _, err := os.Stat(filepath.Join(proxyRoot, "go.mod")); err == nil {
			break
		}
		parent := filepath.Dir(proxyRoot)
		if parent == proxyRoot {
			t.Fatal("could not find jni-proxy root (no go.mod found)")
		}
		proxyRoot = parent
	}
	jniRoot := filepath.Join(filepath.Dir(proxyRoot), "jni")
	if _, err := os.Stat(filepath.Join(jniRoot, "spec")); err != nil {
		t.Fatalf("jni repo not found at %s (need spec/ directory)", jniRoot)
	}
	return jniRoot
}

func TestGenerate_ProducesProtoFile(t *testing.T) {
	root := findJNIRepoRoot(t)
	specPath := filepath.Join(root, "spec", "java", "location.yaml")
	overlayPath := filepath.Join(root, "spec", "overlays", "java", "location.yaml")
	outputDir := t.TempDir()
	goModule := "github.com/AndroidGoLab/jni"

	if err := Generate(specPath, overlayPath, outputDir, goModule); err != nil {
		t.Fatalf("Generate: %v", err)
	}

	protoPath := filepath.Join(outputDir, "location", "location.proto")
	data, err := os.ReadFile(protoPath)
	if err != nil {
		t.Fatalf("read proto file: %v", err)
	}

	content := string(data)

	// Verify syntax declaration.
	if !strings.Contains(content, `syntax = "proto3";`) {
		t.Error("missing proto3 syntax declaration")
	}

	// Verify package declaration.
	if !strings.Contains(content, "package location;") {
		t.Error("missing package declaration")
	}

	// Verify go_package option.
	if !strings.Contains(content, `option go_package = "github.com/AndroidGoLab/jni/proto/location";`) {
		t.Error("missing or incorrect go_package option")
	}

	// Verify generated header.
	if !strings.Contains(content, "Code generated by protogen. DO NOT EDIT.") {
		t.Error("missing generated header")
	}
}

func TestGenerate_MultipleSpecs(t *testing.T) {
	root := findJNIRepoRoot(t)
	outputDir := t.TempDir()
	goModule := "github.com/AndroidGoLab/jni"

	specs := []string{"location", "bluetooth", "toast"}
	for _, name := range specs {
		specPath := filepath.Join(root, "spec", "java", name+".yaml")
		overlayPath := filepath.Join(root, "spec", "overlays", "java", name+".yaml")

		if err := Generate(specPath, overlayPath, outputDir, goModule); err != nil {
			t.Fatalf("Generate %s: %v", name, err)
		}

		protoPath := filepath.Join(outputDir, name, name+".proto")
		if _, err := os.Stat(protoPath); err != nil {
			t.Errorf("expected proto file for %s at %s: %v", name, protoPath, err)
		}
	}
}

func TestGenerate_Idempotency(t *testing.T) {
	root := findJNIRepoRoot(t)
	specPath := filepath.Join(root, "spec", "java", "location.yaml")
	overlayPath := filepath.Join(root, "spec", "overlays", "java", "location.yaml")
	goModule := "github.com/AndroidGoLab/jni"

	outputDir1 := t.TempDir()
	outputDir2 := t.TempDir()

	if err := Generate(specPath, overlayPath, outputDir1, goModule); err != nil {
		t.Fatalf("Generate pass 1: %v", err)
	}
	if err := Generate(specPath, overlayPath, outputDir2, goModule); err != nil {
		t.Fatalf("Generate pass 2: %v", err)
	}

	protoPath1 := filepath.Join(outputDir1, "location", "location.proto")
	protoPath2 := filepath.Join(outputDir2, "location", "location.proto")

	data1, err := os.ReadFile(protoPath1)
	if err != nil {
		t.Fatalf("read pass 1: %v", err)
	}
	data2, err := os.ReadFile(protoPath2)
	if err != nil {
		t.Fatalf("read pass 2: %v", err)
	}

	if string(data1) != string(data2) {
		t.Error("idempotency failure: proto file differs between runs")
	}
}
